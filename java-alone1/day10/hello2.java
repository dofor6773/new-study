public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello, Java333");

        int x;                            // 변수 x선언
        x = 1;                            // 변수 x에 1을 저장
        int y = 2;                        // 변수 y을 선언하고 2를 저장
        int result = x + y; // 1 + 2 = 3  // 변수 result를 선언하고 변수 x와 y를 더한 값을 저장
        System.out.println(result);      // println 메소드 호출
    }  // end of  main메소드
} // end of class